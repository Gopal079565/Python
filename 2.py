# Take input of two number and print there sum , difference , product and quotient.

first_number = int(input("Enter first number :"))
second_number = int(input("Enter second number :"))

print("Sum of two number is :", first_number + second_number)
print("Difference of two number is :",first_number - second_number)
print("Product of two number is :",first_number * second_number)
print("Quotient of two number is :",first_number / second_number )
